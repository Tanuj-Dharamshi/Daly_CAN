/* Send one CAN message and wait for response */
void Send_CAN_Message(uint32_t ext_id)
{
  TxHeader.ExtId = ext_id;
  char buffer[64];

  if (HAL_CAN_AddTxMessage(&hcan, &TxHeader, TxData, &TxMailbox) != HAL_OK)
  {
    UART_Print("Error: Failed to send CAN message\r\n");
    return;
  }

  sprintf(buffer, "Sent CAN ID: 0x%08lX\r\n", ext_id);
  UART_Print(buffer);
}

/* Loop through all CAN IDs and wait for response */
void Process_CAN_Cycle(void)
{
  for (current_index = 0; current_index < total_ids; current_index++)
  {
    response_received = 0;
    Send_CAN_Message(CAN_ExtIDs[current_index]);

    uint32_t startTick = HAL_GetTick();
    while (!response_received && (HAL_GetTick() - startTick < 500))
    {
      // Waiting for response (max 500 ms)
    }

    if (!response_received)
    {
      UART_Print("No response received.\r\n");
    }
  }
}

/* Callback when message is received */
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
  if (HAL_CAN_GetRxMessage(hcan, CAN_RX_FIFO0, &RxHeader, RxData) != HAL_OK)
  {
    UART_Print("Error: Failed to get CAN Rx message\r\n");
    return;
  }

  char buffer[128];
  sprintf(buffer, "Received CAN ID: 0x%08lX | Data: ", RxHeader.ExtId);
  UART_Print(buffer);

  for (int i = 0; i < RxHeader.DLC; i++)
  {
    sprintf(buffer, "%02X ", RxData[i]);
    UART_Print(buffer);
  }
  UART_Print("\r\n");

  response_received = 1;
}
